#!/usr/bin/env node
// =============================================================================
// publish-assessed.js — promote an assessed evidence record to the registry
// and prepare the publication change.
//
// Backs issue #12. Invoked by .github/workflows/growth-publish.yml AFTER the
// #9 validator has confirmed the evidence record passes its hard gate. This
// script:
//   1. reads the latest assessed record for a slug
//   2. if the validator would pass (browser evidence sufficient), promotes the
//      sourceKey to `eligible` in the review registry with an evidenceRef
//   3. emits a PR-body-ready summary (evidence summary + diff summary)
//
// The registry promotion is the catalogue change that ships in the bot PR.
// The PR is capped at five game pages OR one Collection — this script handles
// one slug at a time so the cap is enforced by the workflow's batching.
//
// The model's assessment.decision is NOT trusted here. Only the #9 validator's
// pass counts (ADR-0004). If the record fails validation, the workflow aborts
// before this script runs.
//
// Usage:
//   node scripts/publish-assessed.js --slug=<slug>
//   Prints the PR body markdown to stdout.
// =============================================================================
'use strict';

const fs = require('fs');
const path = require('path');
const { validateRecord } = require('./validate-evidence.js');

const args = Object.fromEntries(
    process.argv.slice(2).map((a) => {
        const m = a.match(/^--([^=]+)=(.*)$/);
        return m ? [m[1], m[2]] : [a.replace(/^--/, ''), true];
    })
);

function die(msg, code = 1) { console.error('✗ ' + msg); process.exit(code); }

const slug = args.slug;
if (!slug) die('required: --slug=<slug>');

const gamesDir = path.resolve(__dirname, '../evidence/games', slug);
if (!fs.existsSync(gamesDir)) die(`no evidence records for "${slug}"`);

// Latest assessed record.
const records = fs.readdirSync(gamesDir).filter((f) => f.endsWith('.json')).sort().reverse();
const recordFile = records[0];
if (!recordFile) die(`no evidence record for "${slug}"`);
const recordPath = path.join(gamesDir, recordFile);
const record = JSON.parse(fs.readFileSync(recordPath, 'utf8'));

// Re-run the validator. If it fails, abort — never publish unvalidated evidence.
const { errors } = validateRecord(recordPath);
if (errors.length) {
    die(`evidence record fails #9 validation (${errors.length} errors) — refusing to publish:\n  ` +
        errors.join('\n  '));
}

// Promote in the registry.
const registryPath = path.resolve(__dirname, '../evidence/review-registry.json');
const registry = JSON.parse(fs.readFileSync(registryPath, 'utf8'));
const sourceKey = record.sourceKey;
if (!registry.states[sourceKey]) die(`sourceKey "${sourceKey}" not in registry`);
const prev = registry.states[sourceKey].state;
registry.states[sourceKey] = {
    state: 'eligible',
    eligibleSince: new Date().toISOString().slice(0, 10),
    provisionalSince: registry.states[sourceKey].provisionalSince,
    evidenceRef: `evidence/games/${slug}/${recordFile}`
};
fs.writeFileSync(registryPath, JSON.stringify(registry, null, 2) + '\n');

// Emit the PR body markdown.
const evidenceRef = `evidence/games/${slug}/${recordFile}`;
const model = record.assessment ? record.assessment.model : 'n/a';
const modelDecision = record.assessment ? record.assessment.decision : 'n/a';
const consoleErrors = record.browser ? record.browser.consoleErrorCount : '?';
const viewports = record.browser && record.browser.viewportResults
    ? record.browser.viewportResults.filter((v) => v.rendered).length + '/2' : '?';

const body = `## Autonomous publication — \`${slug}\`

### Evidence summary
- **Record**: \`${evidenceRef}\`
- **Validator (#9 gate)**: ✅ passed (the arbiter, per ADR-0004)
- **Browser evidence**: loaded=${record.browser && record.browser.loaded}, console errors=${consoleErrors}, viewports rendered=${viewports}
- **Model assessment** (advisory only): provider=${record.assessment && record.assessment.provider}, model=${model}, decision=${modelDecision}
- **Licence**: ${record.source && record.source.licence}

### Registry change
- \`${sourceKey}\`: \`${prev}\` → \`eligible\`

### Diff summary
- \`${evidenceRef}\` (new, immutable)
- \`evidence/review-registry.json\` (state promotion for \`${sourceKey}\`)

### PR scope
- 1 game page promotion (within the ≤5 cap per ADR-0008)
- Generated by \`growth-publish.yml\`; auto-merge requested only after required checks pass.

---
_Automated publication. The model's decision was advisory; the #9 evidence validator is the gate (ADR-0004)._
`;

process.stdout.write(body);
console.error(`\n✓ promoted ${sourceKey}: ${prev} → eligible`);
console.error(`  PR body emitted to stdout`);
